	typedef enum {dead, alive} status_t;
	
	struct sprite{
		int32_t x;	// x coordinate
		int32_t y;	// y coordinate
		int32_t vx, vy;	// velocity. pixels/ 30 Hz
		const unsigned short *image[2];	// ptr -> image. Need 2 images to implement animation for a sprite.
		const unsigned short *black;	// ptr -> black image to cover up the image when sprite is dead.
		status_t life;	// dead/alive
		uint32_t w;	// width
		uint32_t h;	// height
		uint32_t needDraw;	// Semaphore. True if need to draw
	};
	
	// Create sprite types for sprites, lasers, spaceships, etc.	
	typedef struct sprite sprite_t;
	typedef struct sprite laser_t;
	typedef struct sprite spaceship_t;
	
	// Declare an array of sprites called Enemy.	
	sprite_t Enemy[18];	// Starting with 18 enemies.
	//sprite_t Enemy[6];	// Starting with 6 enemies.
	int Flag;	// Semaphore. Tells foreground to redraw image.
	int Anyalive;	// Flag to tell when game is over. When all sprites are dead.
	
	// Declare spaceship		
	spaceship_t SpaceShip;
	
	

//*****************************************************

	void GameInit(void){
		int i;
		Flag = 0;
		
		// Create enemies
		for(i=0;i<6;i++){	// Initially, 6 enemies are alive.
			Enemy[i].x = 20*i;
			Enemy[i].y = 10;
			Enemy[i].vx = 0;	// not moving
			Enemy[i].vy = 0;	// not moving
			
			Enemy[i].image[0] = SmallEnemy10pointA;
			Enemy[i].image[1] = SmallEnemy10pointB;	// These 2 images are to create the animation effect for sprites.
			
			Enemy[i].black = BlackEnemy;   // can be used to cover the previous position of a sprite. Use only if necessary.
			Enemy[i].life = alive;
			Enemy[i].w = 16;
			Enemy[i].h = 10;
			Enemy[i].needDraw = 1;			
		}// for
		
		for(i=0;i<6;i++){	// Move sprites right.
			Enemy[i].vx = 1;
		}		
		
		// Create Spaceship
		SpaceShip.x = 50;
		SpaceShip.y = 160;
		SpaceShip.vx = 1;
		SpaceShip.vy = 0;	// not moving
		SpaceShip.image[0] = ship6;
		SpaceShip.image[1] = ship6;			
		SpaceShip.black = BlackEnemy;
		SpaceShip.life = alive;
		SpaceShip.w = 19;
		SpaceShip.h = 16;
		SpaceShip.needDraw = 1;		
	}// GameInit
	
	
		
	// Executed by interrupt.
	void GameMove(void){
		int i;
		Anyalive = 0;
		
		// Move spaceship
		// Change direction when reaching edges
		if((SpaceShip.x < 0) || (SpaceShip.x > 104)){
			SpaceShip.vx = -1*SpaceShip.vx;
		}				
		SpaceShip.x += SpaceShip.vx;
		
		// Move enemies
		for(i=0;i<6;i++){
			if(Enemy[i].life == alive){
				Enemy[i].needDraw = 1;
				Anyalive = 1;
								
				// Amer Qouneh's code for this structure is clearer than Valvano's.
				// Only one of those conditions is executed and control exits.
				if((Enemy[i].y > 140) || (Enemy[i].y < 10)){
					Enemy[i].life = dead;
				}				
				if((Enemy[i].x < 0) || (Enemy[i].x > 120)){
					Enemy[i].life = dead;
				}				
				Enemy[i].x += Enemy[i].vx;
				Enemy[i].y += Enemy[i].vy;
				
			}// if
			
		}// for		
	}// GameMove
	
	
	
	
	unsigned long FrameCount = 0;	// used for animation of a sprite.
	
	// Called in the main program 
	void GameDraw(void){
		int i;		
		
		for(i=0;i<6;i++){
			if(Enemy[i].needDraw){
				if(Enemy[i].life == alive){
					ST7735_DrawBitmap(Enemy[i].x, Enemy[i].y, Enemy[i].image[FrameCount], Enemy[i].w, Enemy[i].h);	// with animation for sprite
				}else{
					//ST7735_DrawBitmap(Enemy[i].x, Enemy[i].y, Enemy[i].black, Enemy[i].w, Enemy[i].h);	// We can eliminate this line if we don't want to use Black enemies.					
				}
				Enemy[i].needDraw = 0;	// clear the semaphore				
			}// if
		}// for		
		
		// Draw spaceship
		ST7735_DrawBitmap(SpaceShip.x, SpaceShip.y, SpaceShip.image[FrameCount], SpaceShip.w, SpaceShip.h);	
		
		// Draw bunker
		ST7735_DrawBitmap(53, 149, Bunker0, 18, 5);

		FrameCount = (FrameCount+1)&0x01; // 0,1,0,1,... Used for animation to switch between 2 images.
	}// GameDraw
	
	
	
